function cambiaLinguaSito(lang) {
    localStorage.setItem('lingua_scelta', lang);

    const elementiDaTradurre = document.querySelectorAll('[data-lang]');
    
    elementiDaTradurre.forEach(elemento => {
        const chiave = elemento.getAttribute('data-lang');
        if (translations[lang] && translations[lang][chiave]) {
            if (elemento.tagName === 'INPUT' || elemento.tagName === 'TEXTAREA' || elemento.tagName === 'SELECT') {
                elemento.placeholder = translations[lang][chiave];
                if(elemento.tagName === 'SELECT' && elemento.options[0].disabled) {
                     elemento.options[0].text = translations[lang][chiave];
                }
            } else {
                elemento.innerHTML = translations[lang][chiave];
            }
        }
    });

    if (typeof window.initUI === 'function') {
        window.initUI();
    }
    
    window.dispatchEvent(new Event('languageChanged'));
}

document.addEventListener('DOMContentLoaded', () => {
    let linguaSalvata = localStorage.getItem('lingua_scelta');
    
    if (!linguaSalvata) {
        const browserLang = navigator.language || navigator.userLanguage;
        if (browserLang && browserLang.toLowerCase().startsWith('it')) {
            linguaSalvata = 'it';
        } else {
            linguaSalvata = 'en';
        }
    }
    
    cambiaLinguaSito(linguaSalvata);
    
    const flagSpan = document.getElementById('current-flag');
    const testoSpan = document.getElementById('current-lang-text');
    
    if (flagSpan && testoSpan) {
        switch(linguaSalvata) {
            case 'it':
                flagSpan.className = "fi fi-it"; testoSpan.innerText = "IT"; break;
            case 'en':
                flagSpan.className = "fi fi-gb"; testoSpan.innerText = "EN"; break;
            case 'fr':
                flagSpan.className = "fi fi-fr"; testoSpan.innerText = "FR"; break;
            case 'es':
                flagSpan.className = "fi fi-es"; testoSpan.innerText = "ES"; break;
            case 'de':
                flagSpan.className = "fi fi-de"; testoSpan.innerText = "DE"; break;
            default:
                flagSpan.className = "fi fi-gb"; testoSpan.innerText = "EN";
        }
    }
});